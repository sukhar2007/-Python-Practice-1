# Ввод данных от пользователя
доход = float(input("Введите общий доход компании: "))
производственные_расходы = float(input("Введите сумму производственных расходов: "))
административные_расходы = float(input("Введите сумму административных расходов: "))
маркетинговые_расходы = float(input("Введите сумму маркетинговых расходов: "))

# Проверка на корректность данных
if доход < 0 or производственные_расходы < 0 or административные_расходы < 0 or маркетинговые_расходы < 0:
    print("Ошибка: доход и расходы должны быть неотрицательными числами.")
else:
    # Вычисляем общие расходы
    общий_расход = производственные_расходы + административные_расходы + маркетинговые_расходы

    # Проверка, чтобы расходы не превышали доход
    if общий_расход > доход:
        print("Ошибка: расходы не могут превышать доход.")
    else:
        # Вычисляем налоговые льготы
        льготы = (
                производственные_расходы * 0.20 +
                административные_расходы * 0.10 +
                маркетинговые_расходы * 0.05
        )

        # Вычисляем чистую прибыль
        чистая_прибыль = доход - общий_расход + льготы

        # Определение размера налога
        if чистая_прибыль <= 10000:
            налог = чистая_прибыль * 0.10
        elif чистая_прибыль <= 50000:
            налог = чистая_прибыль * 0.20
        else:
            налог = чистая_прибыль * 0.30

        # Вывод результатов
        print(f"Чистая прибыль: {чистая_прибыль:.2f}")
        print(f"Итоговый налог: {налог:.2f}")
